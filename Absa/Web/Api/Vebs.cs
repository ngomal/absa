namespace Absa.Web.Api
{
    public static class Verbs
    {
        /// <summary>
        /// Indicates a read-only get request. Used to retrieve data.
        /// </summary>
        public const string Get = "GET";
        /// <summary>
        /// Indicates a post request. Used to create data.
        /// </summary>
        public const string Post = "POST";
        /// <summary>
        /// Indicates a put request. Used to update data.
        /// </summary>
        public const string Put = "PUT";
        /// <summary>
        /// Indicates a delete request. Used to delete data.
        /// </summary>
        public const string Delete = "DELETE";
    }
}
